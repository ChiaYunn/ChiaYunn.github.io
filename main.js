  // 初始化計數器
  function initCounter() {
    const today = new Date().toDateString();
    const savedDate = localStorage.getItem('counterDate');
    if (savedDate !== today) {
      localStorage.setItem('correctCount', '0');
      localStorage.setItem('counterDate', today);
    }
    updateCounterDisplay();
  }

  // 增加計數
  function incrementCounter() {
    let count = parseInt(localStorage.getItem('correctCount') || '0', 10);
    count += 1;
    localStorage.setItem('correctCount', count.toString());
    updateCounterDisplay();
  }

  // 更新顯示的計數
  function updateCounterDisplay() {
    const count = localStorage.getItem('correctCount') || '0';
    document.getElementById('counter').innerText = `✔️ 今天寫對：${count} 次`;
  }

  // 重播語音
  function replayAudio() {
    if (currentUtter) {
      speechSynthesis.cancel();
      speechSynthesis.speak(currentUtter);
    }
  }

  // 開始筆畫測驗
  function startQuiz() {
    mistakeCount = 0; // 每次新題目時重設

    writer.quiz({
      onMistake: () => {
        mistakeCount += 1;
        const msg = document.getElementById('message');

        if (mistakeCount === 2) {
          msg.innerText = '再錯一次就要重寫囉～';
        } else if (mistakeCount >= 3) {
          msg.innerText = '錯三次了，重新開始寫這個字！';
          mistakeCount = 0;

          // 清除舊的繪製狀態並重建
          const container = document.getElementById('hanzi-container');
          container.innerHTML = '';
          writer = HanziWriter.create(container, currentWord, {
            width: 300,
            height: 300,
            padding: 10,
            showOutline: true,
            showCharacter: false,
            strokeColor: '#000',
            radicalColor: '#000',
            drawingColor: '#000'
          });

          startQuiz(); // 重新開始 quiz
        } else {
          msg.innerText = '筆順錯了，請再試一次～';
        }

        // 自動清除錯誤訊息（不清除第 2 次提示）
        if (mistakeCount !== 2) {
          setTimeout(() => {
            msg.innerText = '';
          }, 700);
        }
      },

      onComplete: () => {
        document.getElementById('message').innerText = '寫對了！好棒！';
        incrementCounter();
      }
    });
  }

  // 載入隨機字元
  function loadRandomCharacter() {
    const grade = document.getElementById('grade-select').value;
    const chars = hanziData[grade];

    if (!chars || chars.length === 0) {
      alert("此年級尚未有字。");
      return;
    }

    const randomChar = chars[Math.floor(Math.random() * chars.length)];
    const { word, zhuyin } = randomChar;
    currentWord = word;

    document.getElementById('question').innerText = `請寫：「${word}」  注音：${zhuyin}`;
    document.getElementById('message').innerText = '';

    const container = document.getElementById('hanzi-container');
    container.innerHTML = ''; // 清除上一題的字形

    writer = HanziWriter.create(container, word, {
      width: 300,
      height: 300,
      padding: 10,
      showOutline: true,
      showCharacter: false,
      strokeColor: '#000',
      radicalColor: '#000',
      drawingColor: '#000'
    });

    startQuiz(); // 開始新的測驗

    // 自動播放語音
    currentUtter = new SpeechSynthesisUtterance(word);
    currentUtter.lang = 'zh-TW';
    currentUtter.rate = 0.8;
    speechSynthesis.cancel();
    speechSynthesis.speak(currentUtter);
  }

