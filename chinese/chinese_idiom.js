let index = 0;       // learn-mode index
let quizIndex = 0;   // quiz-mode index
let hasAnswered = false;  // quiz answered?

// ======== score magenment ========
function initDailyScore() {
    const today = new Date().toISOString().split('T')[0];
    const savedDate = localStorage.getItem('quizDate');
    if (savedDate !== today) {
        localStorage.setItem('quizDate', today);
        localStorage.setItem('dailyCorrect', 0);
    }
    updateScoreDisplay();
}

function updateScoreDisplay() {
    const score = localStorage.getItem('dailyCorrect') || 0;
    document.getElementById("daily-score").textContent = `今日答對次數：${score}`;
}

// ======== learn-mode ========
function renderIdiom(idiomArr) {
    let html = "<table><tr>";
    idiomArr.forEach(item => {
        html += `
            <td class="char">${item.word}</td>
            <td class="zhuyin">${item.zhuyin[0]}</td>
            <td class="zhuyin">${item.zhuyin[1]}</td>
        `;
    });
    html += "</tr></table>";
    return html;
}

function showIdiom(i) {
    document.getElementById("idiom").innerHTML = renderIdiom(idioms[i].idiom);
    document.getElementById("meaning").textContent = idioms[i].meaning;
    document.getElementById("example").textContent = idioms[i].example;
}

function nextIdiom() {
    index = (index + 1) % idioms.length;
    showIdiom(index);
}

// ======== change mode ========
function startMode(mode) {
    document.getElementById("mode-select").classList.add("hidden");

    if (mode === 'learn') {
        document.getElementById("learn-mode").classList.remove("hidden");
        showIdiom(index);
    } else if (mode === 'quiz') {
        document.getElementById("quiz-mode").classList.remove("hidden");
        showQuiz();
    }
}

function backToModeSelect() {
    document.getElementById("learn-mode").classList.add("hidden");
    document.getElementById("quiz-mode").classList.add("hidden");
    document.getElementById("mode-select").classList.remove("hidden");
}

// ======== quiz-mode ========
const allChars = [];
idioms.forEach(item => item.idiom.forEach(ch => allChars.push(ch.word)));

function showQuiz() {
    const item = idioms[quizIndex];
    const missingIndex = Math.floor(Math.random() * item.idiom.length);
    const answer = item.idiom[missingIndex].word;

    hasAnswered = false;

    // show quiz
    let html = "<table><tr>";
    item.idiom.forEach((ch, idx) => {
        if (idx === missingIndex) {
            html += `<td class="char">__</td><td class="zhuyin"> </td><td class="zhuyin"> </td>`;
        } else {
            html += `<td class="char">${ch.word}</td><td class="zhuyin">${ch.zhuyin[0]}</td><td class="zhuyin">${ch.zhuyin[1]}</td>`;
        }
    });
    html += "</tr></table>";
    document.getElementById("quiz-idom").innerHTML = html;
    document.getElementById("quiz-explanation").textContent = item.meaning;

    // Options generation
    const optionsSet = new Set([answer]);
    while (optionsSet.size < 4) {
        const randChar = allChars[Math.floor(Math.random() * allChars.length)];
        optionsSet.add(randChar);
    }

    const opts = Array.from(optionsSet).sort(() => Math.random() - 0.5);
    const optHtml = opts.map(o =>
        `<button class="back-button" onclick="checkAnswer('${o}', '${answer}')">${o}</button>`
    ).join(" ");

    document.getElementById("quiz-options").innerHTML = optHtml;
    document.getElementById("quiz-feedback").textContent = "";
}

function checkAnswer(selected, answer) {
    const feedback = document.getElementById("quiz-feedback");

    if (hasAnswered) {
        feedback.textContent = "你已經回答過了喔～";
        feedback.style.color = "gray";
        return;
    }

    hasAnswered = true;

    if (selected === answer) {
        feedback.textContent = "答對了！🎉🎉🎉";
        feedback.style.color = "green";

        let correct = parseInt(localStorage.getItem('dailyCorrect')) || 0;
        correct += 1;
        localStorage.setItem('dailyCorrect', correct);
        updateScoreDisplay();
    } else {
        feedback.textContent = `錯了，正確答案是：${answer}`;
        feedback.style.color = "red";
    }
}

function nextQuiz() {
    quizIndex = (quizIndex + 1) % idioms.length;
    showQuiz();
}

